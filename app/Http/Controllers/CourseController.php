     */
    public function store(Request $request)
    {
        $request->validate([
            'nama' => 'required|unique:course|max:255',
            'deskripsi' => 'required'
        ]);

        Course::create($request->all());

        return redirect()->route('course.index')->with('success','Input Data Berhasil');
    }

